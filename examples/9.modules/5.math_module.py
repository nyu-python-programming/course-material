import math

my_float = math.pi

# use the built-in __main__ module's round function
new_number = round(my_float)
print(new_number)

# use the math module's ceil function to round up
new_number = math.ceil(my_float)
print(new_number)

# use the math module's ceil function to round down
new_number = math.floor(my_float)
print(new_number)

