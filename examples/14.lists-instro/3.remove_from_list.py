my_list = ['hello', 'goodbye', 'foo', 'bar', 'baz', 'bar'] # a list with 6 values
print(my_list)

my_list.pop(2) # remove one of the values by index
print(my_list)

my_list.remove('bar') # remove the first occurrence of a value
print(my_list)

del my_list[-1] # remove the last value from the list
print(my_list)

